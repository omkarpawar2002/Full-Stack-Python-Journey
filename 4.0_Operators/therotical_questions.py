# What is an operator?
'''
Operators are special symbol which are used to perform operations on operands/variables and values.
'''

# What is an arithmetic operator?
'''
Arithmatic Operators are used to perform mathmatical calculations like addition,subtraction,multiplication,division,floor division,modulus or exponenet.
'''

# Difference between / and //?
'''
1.Division [ / ] will return floating point quotient value.
2.Floor Division [ // ] will return rounded backword integer quotient value.
'''

# What is a comparison operator?
'''
Comparison Operators is used to compare two values and return a boolean result either True or False.
'''

# What type of value do comparison operators return?
'''
Comparison operators return a boolean result which is True or False only.
'''

# What is the difference between = and ==?
'''
1. [ = ] is an assignment operator which is responsible for assigning RHS value to the LHS of variable.
2. [ == ] is an equality operator which return True if both LHS and RHS are equal otherwise they return False.
'''

